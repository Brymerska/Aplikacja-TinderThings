<h1>Regulamin korzystania z aplikacji mobilnej TinderThings<br></h1>
§ 1

POSTANOWIENIA WSTĘPNE<br>



1.      Niniejszy Regulamin określa zasady funkcjonowania aplikacji mobilnej mobiDOM
(„Aplikacja”),udostępnionej przez Administratora na rzecz Użytkowników.<br>

2.      W Regulaminie określone są w szczególności rodzaje i zakres usług świadczonych
drogą elektroniczną, warunki świadczenia usług drogą elektroniczną w tym wymagania techniczne
niezbędne do korzystania z Aplikacji, warunki zawierania i rozwiązywania umów o świadczenie
usług drogą elektroniczną oraz tryb postępowania reklamacyjnego, jak również prawa i
obowiązki jego Użytkowników, oraz zasady odpowiedzialności Administratora i Użytkowników
Aplikacji, zgodnie z ustawą z dnia z dnia 18 lipca 2002 r. o świadczeniu usług drogą elektroniczną
(tekst jedn.: Dz. U. 2013 r. poz. 1422).<br>

3.      Usługi są świadczone na zasadach określonych w Regulaminie oraz p
owszechnie obowiązujących przepisów prawa zaś naruszenie przez
Użytkownika postanowień Regulaminu powodować będzie skutki wynikając
bezpośrednio z treści Regulaminu oraz z w/w przepisów prawa.<br>

4.      Treść niniejszego Regulaminu przekazywany jest Użytkownikowi
nieodpłatnie przed instalacją Aplikacji na urządzeniu mobilnym.
Brak akceptacji Regulaminu uniemożliwia instalację Aplikacji.<br>

<br>
§ 2
<br>
DEFINICJE<br>



Regulamin – oznacza niniejszy regulamin korzystania z aplikacji mobilnej mobiDOM.<br>



Administrator – oznacza spółkę TinderThings.


Aplikacja – aplikację – tj. aplikacja mobilnaTinderThings,
która stanowi kompleksowy przewodnik i kompendium wiedzy inwestora.<br>
Interaktywny dziennik budowy, który pomaga zorganizować i uporządkować cały proces budowy.
Aplikacja dostarcza informacje i rozwiązania, niezbędne w procesie realizacji inwestycji.<br>
Aplikacja składa się z trzech części: PORADNIKA BUDOWY, DZIENNIKA BUDOWY ORAZ ZBIORU POLECANYCH PRODUCENTÓW i PRODUKTÓW.<br>
Jest wzbogacona o GRĘ. Dzięki grywalizacji, aplikacja nie tylko pomaga, ale także bawi i przynosi korzyści – każda aktywność
to punkty – inwestorzy walczą o kolejne poziomy, odznaki i bonusy, m.in. rabaty i nagrody rzeczowe.
Aplikacja jest przygotowana na dwa systemy operacyjne – Android i iOS,  w wersji na smartfony i tablety.
Aplikacja jest bezpłatna - dostępna dla wszystkich i do wszystkich projektów.<br>


<br>
Użytkownik – osoba fizyczna, posiadająca pełną zdolność do czynności prawnych.<br>



Konto - prowadzone dla Użytkownika przez Administratora pod unikalną nazwą<br>
(Login, w tym z możliwością logowania za pomocą danych z platformy facebook.com i plus.google.com)
konto będące zbiorem zasobów, w którym gromadzone są dane Użytkownika oraz informacje o jego działaniach
w ramach Aplikacji, którymi Użytkownik samodzielnie zarządza.



Login - wybrana podczas rejestracji w Aplikacji nazwa Użytkownika,
której Użytkownik używać będzie podczas uzyskiwania dostępu do Konta.
Istnieje możliwość logowania za pomocą posiadanego przez Użytkownika konta w serwisie Facebook (Facebook.com)
lub Google+ (plus.google.com). W takim przypadku dane z tych serwisów są danymi do logowania w Aplikacji.
Login nie może zawierać treści wulgarnych, obraźliwych ani sprzecznych z obowiązującym prawem, nie może
również być tożsamy z loginem innego Użytkownika ani naruszać innych zasad określonych w Regulaminie.



Profil – zgromadzony w ramach Konta zbiór informacji, danych i innych elementów prezentujących i
opisujących osobę danego Użytkownika, przekazywanych dobrowolnie i samodzielnie przez Użytkownika do
Aplikacji, które udostępniane i prezentowane są w Aplikacji.



Usługi – usługi świadczone przez Administratora na rzecz Użytkownika zgodnie z postanowieniami Regulaminu.



Blokada – blokada, trwała lub czasowa, w zależności od uznania Administratora lub osoby przez
niego wyznaczonej, dostępu Użytkownika, który naruszył postanowienia Regulaminu lub przepisów prawa,
do Aplikacji, Konta lub poszczególnych elementów Aplikacji; w sytuacjach wskazanych w Regulaminie,
przewiduje się także czasowe lub trwałe usunięcie Konta Użytkownika.

